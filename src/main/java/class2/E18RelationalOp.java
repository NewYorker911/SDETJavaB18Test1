package class2;

public class E18RelationalOp {

    public static void main(String[] args) {

        int num1=10;
        int num2=20;

        //> greater than; < less than; ==equal; != not equal.

        System.out.println(num1>num2);//false
        System.out.println(num1<num2);//true
        System.out.println(num1==num2);//false
        System.out.println(num1!=num2);//true

    }

}
