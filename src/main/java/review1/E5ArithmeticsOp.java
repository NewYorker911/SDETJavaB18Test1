package review1;

public class E5ArithmeticsOp {

    public static void main(String[] args) {

        int num1=14;
        int num2=2;
        //addition
        System.out.println(num1+num2);
        //subtraction
        System.out.println(num1-num2);
        //multiplication
        System.out.println(num1*num2);
        //division
        System.out.println(num1/num2);
        //modulus
        System.out.println(num1%num2);

    }
}
